const supabase = createClient(supabaseUrl, supabaseServiceKey);

async function setupDetailPages() {
  console.log('üöÄ Configuration de la table detail_pages...');

  try {
    // 1. Cr√©er la table detail_pages
    console.log('üìã Cr√©ation de la table detail_pages...');
    const { error: createError } = await supabase.rpc('exec_sql', {
      sql: `
        CREATE TABLE IF NOT EXISTS detail_pages (
          id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
          card_id UUID REFERENCES cartes(id) ON DELETE CASCADE,
          title TEXT NOT NULL,
          content TEXT NOT NULL,
          meta_description TEXT,
          slug TEXT UNIQUE NOT NULL,
          is_published BOOLEAN DEFAULT false,
          created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
          updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
        );
      `
    });

    if (createError) {
      console.error('‚ùå Erreur lors de la cr√©ation de la table:', createError);
      return;
    }

    console.log('‚úÖ Table detail_pages cr√©√©e avec succ√®s');

    // 2. Cr√©er les index
    console.log('üîç Cr√©ation des index...');
    const indexes = [
      'CREATE INDEX IF NOT EXISTS idx_detail_pages_card_id ON detail_pages(card_id);',
      'CREATE INDEX IF NOT EXISTS idx_detail_pages_slug ON detail_pages(slug);',
      'CREATE INDEX IF NOT EXISTS idx_detail_pages_published ON detail_pages(is_published);'
    ];

    for (const indexSql of indexes) {
      const { error: indexError } = await supabase.rpc('exec_sql', { sql: indexSql });
      if (indexError) {
        console.error('‚ùå Erreur lors de la cr√©ation de l\'index:', indexError);
      }
    }

    console.log('‚úÖ Index cr√©√©s avec succ√®s');

    // 3. Activer RLS
    console.log('üîí Activation de RLS...');
    const { error: rlsError } = await supabase.rpc('exec_sql', {
      sql: 'ALTER TABLE detail_pages ENABLE ROW LEVEL SECURITY;'
    });

    if (rlsError) {
      console.error('‚ùå Erreur lors de l\'activation de RLS:', rlsError);
    } else {
      console.log('‚úÖ RLS activ√©');
    }

    // 4. Cr√©er les politiques RLS
    console.log('üìú Cr√©ation des politiques RLS...');
    const policies = [
      `CREATE POLICY "Pages d√©taill√©es publiques visibles par tous" ON detail_pages
       FOR SELECT USING (is_published = true);`,
      `CREATE POLICY "Admins peuvent tout faire sur les pages d√©taill√©es" ON detail_pages
       FOR ALL USING (
         EXISTS (
           SELECT 1 FROM profiles 
           WHERE profiles.id = auth.uid() 
           AND profiles.role = 'admin'
         )
       );`
    ];

    for (const policySql of policies) {
      const { error: policyError } = await supabase.rpc('exec_sql', { sql: policySql });
      if (policyError && !policyError.message.includes('already exists')) {
        console.error('‚ùå Erreur lors de la cr√©ation de la politique:', policyError);
      }
    }

    console.log('‚úÖ Politiques RLS cr√©√©es');

    // 5. Cr√©er le trigger pour updated_at
    console.log('‚ö° Cr√©ation du trigger updated_at...');
    const triggerSql = `
      CREATE OR REPLACE FUNCTION update_updated_at_column()
      RETURNS TRIGGER AS $$
      BEGIN
          NEW.updated_at = NOW();
          RETURN NEW;
      END;
      $$ language 'plpgsql';

      DROP TRIGGER IF EXISTS update_detail_pages_updated_at ON detail_pages;
      CREATE TRIGGER update_detail_pages_updated_at 
          BEFORE UPDATE ON detail_pages 
          FOR EACH ROW 
          EXECUTE FUNCTION update_updated_at_column();
    `;

    const { error: triggerError } = await supabase.rpc('exec_sql', { sql: triggerSql });
    if (triggerError) {
      console.error('‚ùå Erreur lors de la cr√©ation du trigger:', triggerError);
    } else {
      console.log('‚úÖ Trigger cr√©√©');
    }

    // 6. Ins√©rer des donn√©es d'exemple
    console.log('üìù Insertion de donn√©es d\'exemple...');
    
    // R√©cup√©rer les cartes existantes
    const { data: cards, error: cardsError } = await supabase
      .from('cartes')
      .select('id, title')
      .limit(5);

    if (cardsError) {
      console.error('‚ùå Erreur lors de la r√©cup√©ration des cartes:', cardsError);
      return;
    }

    console.log(`üìä ${cards.length} cartes trouv√©es`);

    // V√©rifier s'il y a d√©j√† des pages d√©taill√©es
    const { data: existingPages, error: pagesError } = await supabase
      .from('detail_pages')
      .select('card_id');

    if (pagesError) {
      console.error('‚ùå Erreur lors de la v√©rification des pages existantes:', pagesError);
      return;
    }

    const existingCardIds = existingPages.map(p => p.card_id);
    const cardsToProcess = cards.filter(card => !existingCardIds.includes(card.id));

    console.log(`üìù Cr√©ation de pages d√©taill√©es pour ${cardsToProcess.length} cartes`);

    for (const card of cardsToProcess) {
      const pageData = {
        card_id: card.id,
        title: `Guide complet ${card.title}`,
        content: `Ceci est un guide d√©taill√© pour ${card.title}. Cette page contient des informations approfondies, des tutoriels, des exemples pratiques et des conseils d'utilisation. Vous y trouverez tout ce dont vous avez besoin pour ma√Ætriser cet outil IA.`,
        meta_description: `Guide complet et d√©taill√© pour ${card.title} - Tutoriels, exemples et conseils pratiques`,
        slug: `guide-${card.title.toLowerCase().replace(/\s+/g, '-')}`,
        is_published: true
      };

      const { error: insertError } = await supabase
        .from('detail_pages')
        .insert([pageData]);

      if (insertError) {
        console.error(`‚ùå Erreur lors de l'insertion pour ${card.title}:`, insertError);
      } else {
        console.log(`‚úÖ Page cr√©√©e pour ${card.title}`);
      }
    }

    // 7. V√©rifier le r√©sultat final
    console.log('üîç V√©rification finale...');
    const { data: finalPages, error: finalError } = await supabase
      .from('detail_pages')
      .select(`
        *,
        card:cartes(title)
      `);

    if (finalError) {
      console.error('‚ùå Erreur lors de la v√©rification finale:', finalError);
    } else {
      console.log(`‚úÖ Configuration termin√©e ! ${finalPages.length} pages d√©taill√©es cr√©√©es`);
      console.log('üìã Pages cr√©√©es:');
      finalPages.forEach(page => {
        console.log(`  - ${page.title} (li√©e √†: ${page.card?.title})`);
      });
    }

  } catch (error) {
    console.error('‚ùå Erreur inattendue:', error);
  }
}

